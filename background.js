// コンテキストメニューの登録
chrome.runtime.onInstalled.addListener(() => {
  chrome.contextMenus.create({
    id: "brushup-yakureki",
    title: "薬歴をAIでブラッシュアップ（SOAP）",
    contexts: ["editable"]
  });
  chrome.contextMenus.create({
    id: "brushup-yakureki-oap",
    title: "薬歴をAIでブラッシュアップ（OAP）",
    contexts: ["editable"]
  });
});

// コンテキストメニュークリック時の処理
chrome.contextMenus.onClicked.addListener(async (info, tab) => {
  const isSOAP = info.menuItemId === "brushup-yakureki";
  const isOAP = info.menuItemId === "brushup-yakureki-oap";
  if (!isSOAP && !isOAP) return;

  const mode = isOAP ? "oap" : "soap";

  try {
    // content.jsからテキストエリアの内容を取得
    const [response] = await chrome.tabs.sendMessage(tab.id, {
      action: "getTextareaValue"
    }).then(r => [r]).catch(() => [null]);

    if (!response) {
      chrome.tabs.sendMessage(tab.id, {
        action: "showError",
        message: "テキストエリアの内容を取得できませんでした"
      });
      return;
    }

    // ローディング表示開始
    chrome.tabs.sendMessage(tab.id, { action: "showLoading" });

    // APIキー・プロバイダー・モデル設定を取得
    const { apiKeys, aiProvider, aiModel } = await chrome.storage.sync.get(["apiKeys", "aiProvider", "aiModel"]);
    const provider = aiProvider || "gemini";
    const model = aiModel || "gemini-flash";
    const apiKey = apiKeys?.[provider];

    if (!apiKey) {
      chrome.tabs.sendMessage(tab.id, { action: "hideLoading" });
      chrome.tabs.sendMessage(tab.id, {
        action: "showError",
        message: "APIキーが設定されていません。拡張機能のアイコンをクリックして設定してください。"
      });
      return;
    }

    // 支Bフラグ・薬Bフラグ確認
    const hasShiB = response.hasShiB === true;
    const hasYakuB = response.hasYakuB === true;
    console.log("[薬歴AI] モード:", mode, "支Bフラグ:", hasShiB, "薬Bフラグ:", hasYakuB, "プロバイダー:", provider, "モデル:", model);

    // AIを呼び出し
    const brushedUp = await callAI(provider, model, apiKey, response.value, response.prescription || "", response.pastData || "", hasShiB, hasYakuB, mode);

    // 結果をテキストエリアに書き戻し
    chrome.tabs.sendMessage(tab.id, {
      action: "setTextareaValue",
      value: brushedUp
    });
  } catch (error) {
    console.error("ブラッシュアップエラー:", error);
    chrome.tabs.sendMessage(tab.id, { action: "hideLoading" });
    chrome.tabs.sendMessage(tab.id, {
      action: "showError",
      message: `エラーが発生しました: ${error.message}`
    });
  }
});

